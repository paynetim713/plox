function detect(){
  try{
    const q = new URLSearchParams(location.search).get("lang"); if(q==="zh"||q==="en") return q;
    const s = localStorage.getItem("plox_lang"); if(s==="zh"||s==="en") return s;
    return (navigator.language||"en").toLowerCase().startsWith("zh") ? "zh" : "en";
  }catch(e){ return "en"; }
}
let LANG = detect();
export function lang(){ return LANG; }
export function setLang(l){ if(l==="en"||l==="zh"){ LANG=l; try{ localStorage.setItem("plox_lang",l); }catch(e){} } }
export function t(k){ const d=DICT[LANG]||DICT.en; return (k in d)? d[k] : (DICT.en[k]!==undefined?DICT.en[k]:k); }
// 把带 data-i18n="key" 的静态元素文本设为 t(key);在 boot 时调用一次
export function applyStatic(){ try{ document.querySelectorAll("[data-i18n]").forEach(function(el){ el.textContent = t(el.getAttribute("data-i18n")); }); }catch(e){} }
